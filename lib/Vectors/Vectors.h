#ifndef LIB_VECTORS_VECTORS_H_
#define LIB_VECTORS_VECTORS_H_

/**
 * @brief A 3d vector.
 * 
 */
typedef struct {
  /**
   * @brief The x component of the vector.
   */
  double x;

  /**
   * @brief The y component of the vector.
   */
  double y;

  /**
   * @brief The z component of the vector.
   */
  double z;
} Vector3;

#endif  // LIB_VECTORS_VECTORS_H_
