#include <Arduino.h>
#include <Wire.h>
#include <lvgl.h>

#include "ui/setup.h"

void setup(void) {
  Serial.begin(9600);
  delay(1000);  // delay for the serial monitor to open
  setup_screen();
}

void loop(void) {
  lv_task_handler();
  lv_tick_inc(5);
  delay(5);
}
